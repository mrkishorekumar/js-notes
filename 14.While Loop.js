//While Loop
/*
	Check for the condition - Execute the block
	1) Initialize - Counter
	2) Condition
	3) Increment
*/
let i = 1;  //counter

while(i<=5){     //condition
	console.log("Hello",i);
	i++;         //increment
}

console.log(i) //6

//Do While
/*
	Execute the  block - Check for the Condition
	1) Initialize - Counter
	2) Increment
	3) Condition
*/
i = 1;  //counter

do{
	console.log("Hello",i);
	i++
}while(i<=5) 

console.log(i)

